package com.sphereon.vdx.ades.enums

/**
 * Defines where to add a signer text inside a signature field relative
 * to an image
 */
enum class SignerTextPosition {
    /**
     * The text on the top of the picture
     */
    TOP,

    /**
     * The text on the bottom of the picture
     */
    BOTTOM,

    /**
     * The text on the right of the picture
     */
    RIGHT,

    /**
     * The text on the left of the picture
     */
    LEFT
}
