package com.sphereon.vdx.ades.enums

/**
 * Enum to define image from text vertical alignment in connection with the
 * image
 */
enum class SignerTextVerticalAlignment {
    /**
     * Align the text with the top of the picture
     */
    TOP,

    /**
     * Align the text with the center of the picture
     */
    MIDDLE,

    /**
     * Align the text with the bottom of the picture
     */
    BOTTOM
}
